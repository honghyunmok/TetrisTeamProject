package team_project;

public interface BoardGUIInterface {
	/*
	 * 게임이 진행되는 구역을 x,y값으로 정하고 해당값을 BoardGUI 클래스로 전달해서 구현한다.
	 * 블록이 실제로 떨어지는 구역의 width값(x축)은 10, height값(y)축은 22로 한다.
	 * 게임이 진행되는 구역 외의 벽(wall) 구현은 하지 않는다(벽 구현은 추후 프로젝트 진행 정도에 따라 현근이가 만든 GUI로 대체)
	 */
	
	/*
	 * GUI 구현은 자체적으로도 가능하겠지만,
	 * Board 클래스에서 값을 가져오는 형식으로 구현할 것.
	 * 가령, Board의 모양을 결정짓는 width와 height 값을 자체 클래스에서 구현하는 것이 아닌 Board 클래스에서 가져올 것
	 */
}
